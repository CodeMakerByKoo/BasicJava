package Methods;

// Predefined Method

// 1) Predefined methods are the method that is already defined in the Java class libraries is known
//    as predefined methods. It is also known as the standard library method or built-in method.
// 2) Some pre-defined methods are length(), equals(), compareTo(), sqrt(), print(), main() etc which
//    can be called in the program at any point.

public class PredefinedMethod {

    public static void main(String[] args) {

        // Using the max() method of Math class
        System.out.println("The maximum number is: " + Math.max(9,7));
    }
}

//    In the above example, we have used three predefined methods main(), print(), and max().
//    We have used these methods directly without declaration because they are predefined.
//    The print() method is a method of PrintStream class that prints the result on the console.
//    The max() method is a method of the Math class that returns the greater of two numbers.
